edad = input("inserta tu edad: ") #input pide datos a ingresar y podemos declarar la variable directamente para que se agrege el valor del input
#todo lo que se agrega a un input es un string, se debe de declarar o convertir a numero o algun otro tipo de dato
print(edad) #imprimimos la variable que tomamos con input

nueva_edad = int(edad) + 5 #convertimos el tipo de dato a entero
print(nueva_edad)

numero = int(input("agrege algun numero: ")) #para declarar una variable directamente como numero se puede poner el tipo de dato y entre parentesis el input para recoger el dato
print(numero)
